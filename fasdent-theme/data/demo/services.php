foreach ( $services as $service ) {
	$existing = get_page_by_path( $service['slug'], OBJECT, 'service' );
	if ( $existing ) {
		$GLOBALS['fasdent_demo_ids']['services'][] = $existing->ID;
		continue;
	}

	$post_id = wp_insert_post(
		array(
			'post_title'   => $service['title'],
			'post_name'    => $service['slug'],
			'post_content' => $service['content'],
			'post_excerpt' => $service['excerpt'],
			'post_status'  => 'publish',
			'post_type'    => 'service',
			'post_author'  => 1,
		),
		true
	);

	if ( is_wp_error( $post_id ) || ! $post_id ) {
		continue;
	}

	// Assign category.
	$term_id = fasdent_demo_get_term_id( $service['category'] );
	if ( $term_id ) {
		wp_set_object_terms( $post_id, array( $term_id ), 'service_category' );
	}

	// Meta.
	update_post_meta( $post_id, 'service_price', $service['price'] );
	update_post_meta( $post_id, 'service_duration', $service['duration'] );
	update_post_meta( $post_id, 'service_icon', $service['icon'] );
	update_post_meta( $post_id, 'service_faqs_json', wp_json_encode( $service['faqs'], JSON_UNESCAPED_UNICODE ) );
	update_post_meta( $post_id, 'service_steps_json', wp_json_encode( $service['steps'], JSON_UNESCAPED_UNICODE ) );
	update_post_meta( $post_id, 'service_benefits_json', wp_json_encode( $service['benefits'], JSON_UNESCAPED_UNICODE ) );
	update_post_meta( $post_id, 'related_services_slugs', $service['related'] );

	$GLOBALS['fasdent_demo_ids']['services'][] = $post_id;
}
