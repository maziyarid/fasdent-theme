foreach ( $faqs as $faq ) {
	$existing = get_page_by_path( $faq['slug'], OBJECT, 'faq' );
	if ( $existing ) {
		$GLOBALS['fasdent_demo_ids']['faqs'][] = $existing->ID;
		continue;
	}

	$post_id = wp_insert_post(
		array(
			'post_title'   => $faq['title'],
			'post_name'    => $faq['slug'],
			'post_content' => $faq['content'],
			'post_status'  => 'publish',
			'post_type'    => 'faq',
			'post_author'  => 1,
		),
		true
	);

	if ( is_wp_error( $post_id ) || ! $post_id ) {
		continue;
	}

	$GLOBALS['fasdent_demo_ids']['faqs'][] = $post_id;
}
